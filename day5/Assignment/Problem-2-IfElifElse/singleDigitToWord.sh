read -p "Enter a single digit number: " i

if [[ $i -eq 0 ]]
then
echo "zero"
elif [[ $i -eq 1 ]]
then
echo "one"
elif [[ $i -eq 2 ]]
then
echo "two"
elif [[ $i -eq 3 ]]
then
echo "three"
elif [[ $i -eq 4 ]]
then
echo "four"
elif [[ $i -eq 5 ]]
then
echo "five"
elif [[ $i -eq 6 ]]
then
echo "six"
elif [[ $i -eq 7 ]]
then
echo "seven"
elif [[ $i -eq 8 ]]
then
echo "eight"
else [[ $i -eq 9 ]]
echo "nine"
fi
